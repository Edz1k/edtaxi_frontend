// Что именно пассажир выбирает точкой на карте. 'stop' — промежуточная
// остановка; какая именно, знает стор (mapPickerStopIndex): по цепочке
// компонентов ходит только режим, чтобы не тащить индекс через каждый проп.
//
// 'trip-stop' — остановка, добавляемая уже В ИДУЩЕЙ поездке. Это отдельный
// режим, а не 'stop', потому что результат уходит в другое место: 'stop' правит
// черновик заказа, а 'trip-stop' — заявку на бэкенд, которую ещё должен
// подтвердить водитель.
export type MapPickerMode = 'destination' | 'pickup' | 'stop' | 'trip-stop'
