import type { GeoPlace, TripRoute } from '@edtaxi/shared/types/geocoding'

// applyPickupSnap двигает точку подачи на проезжую часть по поправке бэкенда
// (п.41): пин пассажира и особенно автоматическая GPS-точка регулярно попадают
// в середину здания, а машина всё равно подъедет к дороге.
//
// Применять результат обязательно до формирования заказа: если отправить
// исходную координату, сервер поправит её сам, и пин у пассажира разойдётся с
// точкой, куда поедет водитель.
//
// Адрес и прочие поля места сохраняются: пассажир выбирал именно этот адрес,
// меняются только координаты — на десятки метров, до ближайшего проезда.
// Порог разумности (дальше которого поправка не присылается) считает бэкенд.
export function applyPickupSnap(pickup: GeoPlace, route: TripRoute): GeoPlace {
  const snapped = route.snapped_pickup
  if (!snapped)
    return pickup

  return { ...pickup, lat: snapped.lat, lng: snapped.lng }
}
